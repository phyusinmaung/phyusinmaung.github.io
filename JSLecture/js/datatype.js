// String data type
let name = "Phyu Sin Maung";
console.log(name);
console.log(typeof(name));

// Numeric data type
let age = 21;
console.log(age);
console.log(typeof(age));

// Boolean data type 0,1
let single = true;
console.log(single);
console.log(typeof(single));

// Undefined
let myBaby;
console.log(myBaby);

// Null
let son = null;
console.log(son);
console.log(typeof(son));

// Array
let arr1= new Array('a', 'b', 'c');
console.log(arr1);
console.log(typeof(arr1));

// Multiarray
let multi = ('Mg Mg', 'Aung Aung', 'Hla Hla', ('Tun Tun', 'Mya Mya'), 'Aye Aye');
console.log(multi);
console.log(typeof(multi));

// Object
let student = {
    // key : value ,
    name : 'Phyu Sin',
    age : 21,
    address : "Yangon",
    education : "Student"
}
console.log(student);
console.log(typeof(student));

