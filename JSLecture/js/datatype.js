// String data type
let name = "Phyu Sin Maung";
console.log(name);
console.log(typeof(name));

// Numeric data type
let age = 21;
console.log(age);
console.log(typeof(age));

// Boolean data type 0,1
let single = true;
console.log(single);
console.log(typeof(single));

// Undefined
let myBaby;
console.log(myBaby);

// Null
let son = null;
console.log(son);
console.log(typeof(son));

